shop = [['каретка', 1200], ['шатун', 1000], ['седло', 300],
        ['педаль', 100], ['седло', 1500], ['рама', 12000],
        ['обод', 2000], ['шатун', 200], ['седло', 2700]]

fragen = input("Название детали: ")

p = 0
c = 0
for detal in shop:
        if fragen == detal[0]:
                p += detal[1]
                c += 1
print("Кол-во деталей -", c)
print("Общая стоимость -", p)